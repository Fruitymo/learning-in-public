# Program for simple calculations

#write a method to add to numbers a and b
def add(a,b):
    return a+b
#write a method to subtract two numbers a and b
def subtract(a,b):
    return a-b
#write a method to multiply two numbers a and b
def multiply(a,b):
    return a*b
#write a method to divide two numbers a and b
def divide(a,b):
    return a/b
#write a method to calculate the remainder of two numbers a and b
def remainder(a,b):
    return a%b

# Test the above methods
#include the method name as a string in the print statement
print("Addition of 2 and 3 is", add(2,3))
print("Subtraction of 2 and 3 is", subtract(2,3))
print("Multiplication of 2 and 3 is", multiply(2,3))
print("Division of 2 and 3 is", divide(2,3))
print("Remainder of 2 and 3 is", remainder(2,3))